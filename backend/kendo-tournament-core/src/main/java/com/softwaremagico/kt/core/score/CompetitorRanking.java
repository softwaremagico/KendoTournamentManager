public class CompetitorRanking {

    private int ranking;

    private int total;

    public CompetitorRanking(int ranking, int total) {
        this.ranking = ranking;
        this.total = total;
    }

    public int getRanking() {
        return ranking;
    }

    public void setRanking(int ranking) {
        this.ranking = ranking;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
